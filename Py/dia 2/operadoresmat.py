# Operaciones Simples
print(f"Suma: 10 + 5 = {10 + 5}")
print(f"Resta: 20 - 8 = {20 - 8}")
print(f"Multiplicación: 7 * 6 = {7 * 6}")

# Divisiones
print(f"División real: 10 / 3 = {10 / 3}")
print(f"División al piso: 10 // 3 = {10 // 3}")
print(f"Resto (Módulo): 10 % 3 = {10 % 3}")

# Potencias y Raíces
print(f"Potencia (2 al cubo): 2 ** 3 = {2 ** 3}")
print(f"Raíz cuadrada de 16: 16 ** 0.5 = {16 ** 0.5}")