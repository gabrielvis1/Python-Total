# Pedimos las dos palabras para el nombre
palabra1 = input("Escribe una palabra que defina tu producto: ")
palabra2 = input("Escribe un animal o concepto que te guste: ")

# Combinamos las palabras en una sola variable
nombre_final = palabra1 + palabra2

# Imprimimos el resultado final
# \n genera el salto de línea
# \" permite mostrar las comillas dobles
print("El nombre de tu producto es\n" + "\"" + nombre_final + "\"")